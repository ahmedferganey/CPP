#include <iostream>
#include <cstddef>




/////////////////////////////////////////////////////////////////////////////////////
//  inline-fun
// Generic Programming
/////////////////////////////////////////////////////////////////////////////////////
inline int inlinefun(int n) { return (n > 1) ? inlinefun(n - 1) + inlinefun(n - 2) : 1; }


/////////////////////////////////////////////////////////////////////////////////////
// constexpr-fun
// Generic Programming
/////////////////////////////////////////////////////////////////////////////////////
constexpr int constexprfun(int n) { return (n > 1) ? constexprfun(n - 1) + constexprfun(n - 2) : 1; }


int main() {


/////////////////////////////////////////////////////////////////////////////////////
//  inline-fun
// Generic Programming
/////////////////////////////////////////////////////////////////////////////////////
  int x = inlinefun(10);
  std::cout << x << "\n";



/////////////////////////////////////////////////////////////////////////////////////
//  constexpr-fun
// Generic Programming
/////////////////////////////////////////////////////////////////////////////////////

  constexpr int y = constexprfun(10);
  std::cout << y << "\n";



}
    










